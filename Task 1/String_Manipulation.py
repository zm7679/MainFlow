#String Manipulation
# Concatenation, slicing, length, upper/lower case
my_string = "Hello, World!"
concatenated = my_string + " How are you?"
sliced = my_string[7:12]
length = len(my_string)
upper_case = my_string.upper()
lower_case = my_string.lower()

print(f"Concatenated: {concatenated}, Sliced: {sliced}, Length: {length}, Upper case: {upper_case}, Lower case: {lower_case}")
